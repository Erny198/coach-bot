# Человекочитаемые названия режимов и какой одиночный тариф их открывает.
MODE_LABEL = {
    "coach": "🧭 Коуч",
    "coachee": "🎓 Коучи",
    "trainer": "🥊 Тренер",
}
MODE_TO_SUGGESTED_TIER = {
    "coach": "coach",
    "coachee": "trainer",
    "trainer": "trainer",
}


def _rub(n) -> str:
    return f"{int(n):,}".replace(",", " ")


def paywall_text(bot_mode: str, reason: str) -> str:
    label = MODE_LABEL.get(bot_mode, "этот режим")
    tier = MODE_TO_SUGGESTED_TIER.get(bot_mode, "all")
    p_mode = pricing.price(tier, "month", "rub")
    p_all = pricing.price("all", "month", "rub")
    title = pricing.TIERS.get(tier, {}).get("title", "режим")

    if reason == "limit":
        head = f"Бесплатные действия в режиме {label} закончились."
    else:  # paywall (премиум-режим без бесплатных действий)
        head = f"Режим {label} доступен по подписке."

    return (
        f"{head}\n\n"
        f"Открой «{title}» — ₽{_rub(p_mode)}/мес, "
        f"или «Всё включено» (Коуч + Тренер + Супервизор) — ₽{_rub(p_all)}/мес.\n\n"
        f"Это дешевле одной живой сессии. Продолжим практику?"
    )


