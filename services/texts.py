def pricing_overview() -> str:
    lines = ["💳 Тарифы (₽/мес · ₽/год):\n"]
    order = ["coach", "trainer", "supervisor",
             "coach_trainer", "trainer_supervisor", "coach_supervisor", "all"]
    for key in order:
        t = pricing.TIERS[key]
        star = " ⭐" if key == "all" else ""
        lines.append(f"• {t['title']}{star} — ₽{_rub(t['rub_month'])} / ₽{_rub(t['rub_year'])}")
    lines.append("\nГодовой план = цена 10 месяцев (≈2 месяца в подарок).")
    return "\n".join(lines)
